<?php

namespace UserManagementBundle\Entity;

/**
 * UserPhone
 */
class UserPhone
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var integer
     */
    private $number;

    /**
     * @var \UserManagementBundle\Entity\User
     */
    private $user;


    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set number
     *
     * @param integer $number
     *
     * @return UserPhone
     */
    public function setNumber($number)
    {
        $this->number = $number;

        return $this;
    }

    /**
     * Get number
     *
     * @return integer
     */
    public function getNumber()
    {
        return $this->number;
    }

    /**
     * Set user
     *
     * @param \UserManagementBundle\Entity\User $user
     *
     * @return UserPhone
     */
    public function setUser(\UserManagementBundle\Entity\User $user = null)
    {
        $this->user = $user;

        return $this;
    }

    /**
     * Get user
     *
     * @return \UserManagementBundle\Entity\User
     */
    public function getUser()
    {
        return $this->user;
    }
}
